export const UNIVERSE_MAP: Record<string, StockEntry[]> = {
  IN: INDIA_UNIVERSE,
  US: USA_UNIVERSE,
  CN: CHINA_UNIVERSE,
  JP: JAPAN_UNIVERSE,
  CMDTY: COMMODITIES_UNIVERSE,
};

export function searchStocks(marketId: string, query: string, limit = 10): StockEntry[] {
  if (!query.trim()) return [];
  const universe = UNIVERSE_MAP[marketId] ?? [];
  const q = query.toLowerCase();
  const scored = universe.map(s => {
    const symMatch = s.sym.toLowerCase().startsWith(q) ? 3 : s.sym.toLowerCase().includes(q) ? 2 : 0;
    const nameMatch = s.name.toLowerCase().startsWith(q) ? 2 : s.name.toLowerCase().includes(q) ? 1 : 0;
    const sectorMatch = s.sector.toLowerCase().includes(q) ? 0.5 : 0;
    return { entry: s, score: symMatch + nameMatch + sectorMatch };
  }).filter(x => x.score > 0);
  scored.sort((a, b) => b.score - a.score);
  return scored.slice(0, limit).map(x => x.entry);
}
